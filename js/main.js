for(let count = 1; count <= 100; count++){

    if(count % 15 == 0){
        console.log(count + "回目　アホじゃない犬");
    }
            
    else if(count % 3 == 0){
        console.log(count + "回目　アホ");
    }
            
    else if(count % 5 == 0){
        console.log(count + "回目　犬");
    }
            
    else{
        console.log(count + "回目");
        }
}
